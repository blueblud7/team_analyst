export function normalizeSector(raw: string): CanonicalSector | null {
  const key = raw.trim().toLowerCase()
  return ALIAS_MAP[key] ?? null
}

// Normalize array, deduplicate, preserve order, drop unknowns
export function normalizeSectors(raws: string[]): CanonicalSector[] {
  const seen = new Set<CanonicalSector>()
  const result: CanonicalSector[] = []
  for (const raw of raws) {
    const canonical = normalizeSector(raw)
    if (canonical && !seen.has(canonical)) {
      seen.add(canonical)
      result.push(canonical)
    }
  }
  return result
}

